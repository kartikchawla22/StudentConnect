function getValidSemesters() {
    const semesters = ['Summer', 'Winter', 'Fall']
    const currentYear = new Date().getFullYear();
    const semesterArr = semesters.map((sem) => `${sem}-${currentYear}`).concat(semesters.map((sem) => `${sem}-${currentYear - 1}`)).concat(semesters.map((sem) => `${sem}-${currentYear + 1}`));
    semesterObj = semesterArr.reduce((acc, key) => {
        acc.push({
            label: key,
            value: key.toLowerCase().replace('-', '')
        })
        return acc
    }, [])
    return semesterObj
}